# sumar dos numeros de manera dinamica 
numero1=int(input("digite el numero1: "))
numero2=int(input("digite el numero2: "))
suma=numero1+numero2
resta=numero1-numero2
divicion=numero1/numero2
multiplicacion=numero1*numero2
promedio=(numero1+numero2)/2
porcentaje=numero1%numero2
print("el resultado de la suma es:" ,suma)
print("el resultado de la resta es:" ,resta)
print("el resultado de la divicin es:" , divicion)
print("el resultado de la multiplicacion es:" , multiplicacion)
print("el resultado del promedio es:" , promedio)
print("el resultado del porcentaje es:" , porcentaje)